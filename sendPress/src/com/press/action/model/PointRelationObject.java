package com.press.action.model;

public class PointRelationObject {
	private String id;
	private String printPointName;
	private String sendPointName;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPrintPointName() {
		return printPointName;
	}
	public void setPrintPointName(String printPointName) {
		this.printPointName = printPointName;
	}
	public String getSendPointName() {
		return sendPointName;
	}
	public void setSendPointName(String sendPointName) {
		this.sendPointName = sendPointName;
	}
	
}

